    <h1>About</h1>

    <p>Bristol Reinforcement Learning (BristolRL) is a society primarily consisting of post-graduate students (but we welcome all) who are researching or have a general interest in reinforcement learning. </p>
    <p>We are an active and sociable group with weekly meetings which involve talking, presenting, and discussing the latest research with a chance for students to present their latest work.</p>
    <p>There are also opportunities to join student-led sessions which in the past have included watching and discussing online lectures, group-reading of textbooks, and implementing algorithms.</p>
    <p>We also run socials which include pub trips and board games. The society is a great way to meet people with similar academic interests with no prerequisite to joining apart from a willingness to learn and interact in the group.</p>    

